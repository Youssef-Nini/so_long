#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		ft_putstr("(null)", len);
		return ;
	}
	while (s[i])
		ft_putchar(s[i++], len);
}
